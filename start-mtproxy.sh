#!/bin/bash
###############################################################################
# start-mtproxy.sh — обёртка для быстрой инициализации и запуска MTProxy
#
# Этот файл обеспечивает обратную совместимость: пользователи, привыкшие
# запускать `bash start-mtproxy.sh`, получают тот же результат — прокси
# запускается. Но теперь под капотом используется новый CLI `mtpx`.
#
# Логика:
#   1. Проверяем, что mtpx существует
#   2. Запускаем `mtpx init` (безопасно: если уже инициализировано — no-op)
#   3. Если нет активных секретов — генерируем один
#   4. Запускаем `mtpx apply` (остановить старый → запустить новый)
#   5. Выводим ссылку для подключения
#
# Для полного контроля рекомендуется использовать `./mtpx` напрямую.
###############################################################################
set -euo pipefail

# ─────────────────────────────────────────────────────────────────────────────
# Определяем путь к mtpx
# ─────────────────────────────────────────────────────────────────────────────
# SCRIPT_DIR — директория, в которой лежит start-mtproxy.sh.
# MTPX — путь к CLI-утилите в той же директории.
# ─────────────────────────────────────────────────────────────────────────────
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
MTPX="${SCRIPT_DIR}/mtpx"

# ─────────────────────────────────────────────────────────────────────────────
# Проверка наличия mtpx
# ─────────────────────────────────────────────────────────────────────────────
# Если mtpx удалён или перемещён — выводим понятную ошибку вместо
# стандартного bash "file not found".
# ─────────────────────────────────────────────────────────────────────────────
if [[ ! -f "${MTPX}" ]]; then
  echo "❌ Не найден mtpx CLI: ${MTPX}"
  echo "   Убедитесь, что файлы проекта на месте."
  exit 1
fi

echo "🚀 Запуск MTProto прокси через mtpx CLI"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"

# ─────────────────────────────────────────────────────────────────────────────
# Шаг 1: Инициализация (если ещё не инициализировано)
# ─────────────────────────────────────────────────────────────────────────────
# `mtpx init` идемпотентен: если файлы уже созданы, он не перезаписывает их.
# 2>/dev/null || true — подавляем ошибки (уже инициализировано = ок).
# ─────────────────────────────────────────────────────────────────────────────
bash "${MTPX}" init 2>/dev/null || true

# ─────────────────────────────────────────────────────────────────────────────
# Шаг 2: Генерация секрета (если нет активных)
# ─────────────────────────────────────────────────────────────────────────────
# `mtpx secret list` выводит таблицу; ищем строки со словом "active".
# grep -c считает совпадения. Если 0 — добавляем секрет типа fake_tls.
# ─────────────────────────────────────────────────────────────────────────────
ACTIVE=$(bash "${MTPX}" secret list 2>/dev/null | grep -c "active" || echo "0")
if [[ "$ACTIVE" -eq 0 ]]; then
  echo "🔑 Генерация секрета..."
  bash "${MTPX}" secret add fake_tls
  echo ""
fi

# ─────────────────────────────────────────────────────────────────────────────
# Шаг 3: Применение конфигурации
# ─────────────────────────────────────────────────────────────────────────────
# `mtpx apply` — основная команда:
#   • Останавливает старый контейнер (если есть)
#   • Берёт первый активный секрет из secrets.csv
#   • Запускает новый контейнер telegrammessenger/proxy
#   • Обновляет runtime.env
# ─────────────────────────────────────────────────────────────────────────────
echo "📦 Запуск прокси..."
echo ""
bash "${MTPX}" apply

# ─────────────────────────────────────────────────────────────────────────────
# Шаг 4: Ссылка для подключения
# ─────────────────────────────────────────────────────────────────────────────
# `mtpx secret link` генерирует tg://-ссылку с сервером, портом и секретом.
# Клик по ней в Telegram (на телефоне/десктопе) автоматически добавит прокси.
# ─────────────────────────────────────────────────────────────────────────────
echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "🔗 Ссылка для подключения:"
bash "${MTPX}" secret link
echo ""
echo "💡 Полный контроль: ./mtpx help"
