#!/usr/bin/env bash
###############################################################################
# lib/config.sh — управление конфигурацией
#
# Отвечает за:
#   • config/domains.txt — список доменов (первый = текущий активный)
#   • state/runtime.env — runtime-параметры (порт, контейнер, время apply)
#
# Все операции с файлами используют атомарную запись (через util.sh),
# чтобы не повредить конфиги при обрыве процесса.
###############################################################################
set -euo pipefail

# Подключаем утилиты (atomic_write, validate_domain, и т.д.)
# shellcheck source=lib/util.sh
source "${MTPX_ROOT}/lib/util.sh"

# ─────────────────────────────────────────────────────────────────────────────
# Domains — управление списком доменов
# ─────────────────────────────────────────────────────────────────────────────
# domains.txt — простой текстовый файл, один домен на строку.
# Текущий (активный) домен — всегда первая строка.
# Это позволяет легко ротировать: перемещаем первый в конец,
# и новый первый становится активным.
# ─────────────────────────────────────────────────────────────────────────────

# ── domains_init — инициализация файла доменов ───────────────────────────────
# Создаём domains.txt с доменом по умолчанию (ya.ru), если файл ещё не
# существует. Не перезаписываем существующий — это важно при повторном
# запуске `mtpx init`.
# ─────────────────────────────────────────────────────────────────────────────
domains_init() {
  if [[ ! -f "${DOMAINS_FILE}" ]]; then
    atomic_write "${DOMAINS_FILE}" "${DEFAULT_DOMAIN}"
    log_info "Создан ${DOMAINS_FILE} с доменом по умолчанию: ${DEFAULT_DOMAIN}"
  fi
}

# ── domain_current — получить текущий (активный) домен ───────────────────────
# Читает первую строку domains.txt.
# Если файл пуст или отсутствует — ошибка.
# tr -d '\r' — защита от Windows-переносов строк (CRLF → LF).
# ─────────────────────────────────────────────────────────────────────────────
domain_current() {
  if [[ ! -f "${DOMAINS_FILE}" ]]; then
    log_error "Файл доменов не найден: ${DOMAINS_FILE}"
    return 1
  fi
  local current
  current=$(head -1 "${DOMAINS_FILE}" | tr -d '\r')
  if [[ -z "$current" ]]; then
    log_error "Список доменов пуст"
    return 1
  fi
  printf '%s' "$current"
}

# ── domain_list — вывести все домены ─────────────────────────────────────────
# Просто cat файла. Если файл не существует — пустой вывод (не ошибка).
# ─────────────────────────────────────────────────────────────────────────────
domain_list() {
  if [[ ! -f "${DOMAINS_FILE}" ]]; then
    echo ""
    return 0
  fi
  cat "${DOMAINS_FILE}"
}

# ── domain_set — установить домен как текущий ────────────────────────────────
# (Оставлено для обратной совместимости, но не используется в v3)
domain_set() {
  local domain="$1"
  validate_domain "$domain"
  if [[ ! -f "${DOMAINS_FILE}" ]]; then
    log_error "Файл доменов не найден"
    return 1
  fi
  if ! grep -qxF "$domain" "${DOMAINS_FILE}" 2>/dev/null; then
    log_error "Домен '$domain' не найден в списке."
    return 1
  fi
  local tmp
  tmp="$(mktemp "${DOMAINS_FILE}.tmp.XXXXXX")"
  printf '%s\n' "$domain" > "$tmp"
  grep -vxF "$domain" "${DOMAINS_FILE}" >> "$tmp" 2>/dev/null || true
  mv -f "$tmp" "${DOMAINS_FILE}"
  log_info "Текущий домен установлен: $domain"
}

# ─────────────────────────────────────────────────────────────────────────────
# Runtime — управление runtime-параметрами
# ─────────────────────────────────────────────────────────────────────────────
# runtime.env — файл в формате KEY=VALUE, хранит:
#   PORT         — порт, на котором запущен прокси
#   CONTAINER    — имя контейнера
#   LAST_APPLY   — timestamp последнего применения конфигурации
#   SECRET       — замаскированный текущий секрет (для отображения)
# ─────────────────────────────────────────────────────────────────────────────

# ── runtime_init — инициализация runtime-файла ───────────────────────────────
# Создаём runtime.env с параметрами по умолчанию.
# chmod 600 — файл может содержать чувствительные данные.
# ─────────────────────────────────────────────────────────────────────────────
runtime_init() {
  if [[ ! -f "${RUNTIME_FILE}" ]]; then
    cat > "${RUNTIME_FILE}" <<EOF
PORT=${DEFAULT_PORT}
CONTAINER=${CONTAINER_NAME}
LAST_APPLY=never
EOF
    chmod 600 "${RUNTIME_FILE}"
    log_info "Создан ${RUNTIME_FILE}"
  fi
}

# ── runtime_get / runtime_set — обёртки над env_get / env_set ────────────────
# Упрощённый API: не нужно указывать путь к файлу — он фиксирован.
# ─────────────────────────────────────────────────────────────────────────────
runtime_get() {
  env_get "${RUNTIME_FILE}" "$1"
}

runtime_set() {
  env_set "${RUNTIME_FILE}" "$1" "$2"
}

# ─────────────────────────────────────────────────────────────────────────────
# config_summary — текстовая сводка конфигурации
# ─────────────────────────────────────────────────────────────────────────────
# Выводит таблицу с текущим доменом, количеством доменов и портом.
# Используется в статусе и для быстрой проверки конфигурации.
# ─────────────────────────────────────────────────────────────────────────────
config_summary() {
  echo "┌─────────────────────────────────────────┐"
  echo "│  Конфигурация                           │"
  echo "├─────────────────────────────────────────┤"
  if [[ -f "${DOMAINS_FILE}" ]]; then
    local current
    current=$(head -1 "${DOMAINS_FILE}" | tr -d '\r')
    local total
    total=$(wc -l < "${DOMAINS_FILE}")
    echo "│  Домен (тек.):  ${current}"
    echo "│  Доменов всего: ${total}"
  else
    echo "│  Домены: не инициализированы          │"
  fi
  if [[ -f "${RUNTIME_FILE}" ]]; then
    local port
    port=$(env_get "${RUNTIME_FILE}" "PORT")
    echo "│  Порт:            ${port:-N/A}"
  fi
  echo "└─────────────────────────────────────────┘"
}
