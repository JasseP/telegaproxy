#!/usr/bin/env bash
###############################################################################
# lib/util.sh — общие утилиты
#
# Базовый модуль, от которого зависят все остальные. Предоставляет:
#   • Цветной вывод (с автоопределением: терминал → цвета, pipe → без цветов)
#   • Единообразное логирование (info / warn / error / step / ok)
#   • Определение путей проекта (MTPX_ROOT, config/, state/, lib/)
#   • Атомарную запись файлов через temp + mv (защита от обрыва на полпути)
#   • Установку chmod 600 на чувствительные файлы (секреты, runtime)
#   • Валидацию порта, домена, секретов
#   • Генерацию Fake TLS и простых секретов
#   • Проверку наличия внешних команд (docker, openssl, ...)
#   • Чтение/запись KEY=VALUE из .env-файлов
#   • Маскировку секретов при выводе (показываются первые 6 и последние 4 символа)
#   • Определение публичного IP сервера
#   • Проверку занятости порта (ss или netstat)
###############################################################################
set -euo pipefail

# ─────────────────────────────────────────────────────────────────────────────
# Цвета для терминального вывода
# ─────────────────────────────────────────────────────────────────────────────
# Проверяем, является ли stdout терминалом (-t 1).
# Если вывод перенаправлен в файл/pipe — отключаем ANSI-коды, чтобы не
# засорять логи управляющими последовательностями.
if [[ -t 1 ]]; then
  GREEN='\033[0;32m'   # зелёный — успешные операции
  RED='\033[0;31m'     # красный — ошибки
  YELLOW='\033[1;33m'  # жёлтый — предупреждения
  BLUE='\033[0;34m'    # синий — шаги / заголовки
  CYAN='\033[0;36m'    # голубой — зарезервирован
  NC='\033[0m'         # сброс
else
  GREEN=''; RED=''; YELLOW=''; BLUE=''; CYAN=''; NC=''
fi

# ─────────────────────────────────────────────────────────────────────────────
# Логирование
# ─────────────────────────────────────────────────────────────────────────────
# Каждая функция — однострочная обёртка над echo -e.
# >&2 перенаправляет warn/error в stderr, чтобы не смешивать с stdout
# (полезно при скриптовой обработке вывода).
# ─────────────────────────────────────────────────────────────────────────────

# [✓] Информация / успешное действие
log_info()  { echo -e "${GREEN}[✓]${NC} $*"; }

# [!] Предупреждение — не критично, но стоит обратить внимание
log_warn()  { echo -e "${YELLOW}[!]${NC} $*" >&2; }

# [✗] Ошибка — действие не выполнено
log_error() { echo -e "${RED}[✗]${NC} $*" >&2; }

# ▸ Начало шага / операции
log_step()  { echo -e "${BLUE}▸${NC} $*"; }

#   готово — короткое подтверждение завершения шага
log_ok()    { echo -e "${GREEN}  готово${NC}"; }

# ─────────────────────────────────────────────────────────────────────────────
# Пути проекта
# ─────────────────────────────────────────────────────────────────────────────
# MTPX_ROOT определяется динамически: берём путь скрипта, из которого был
# вызван данный модуль (BASH_SOURCE[1] — родительский скрипт, обычно mtpx),
# и поднимаемся на уровень выше (..). Если BASH_SOURCE[1] недоступен,
# используется BASH_SOURCE[0] (сам util.sh).
# Переменная экспортируется, чтобы дочерние процессы видели корень проекта.
# ─────────────────────────────────────────────────────────────────────────────
: "${MTPX_ROOT:="$(cd "$(dirname "$(readlink -f "${BASH_SOURCE[1]:-${BASH_SOURCE[0]}}")")/.." && pwd)"}"
export MTPX_ROOT

# Абсолютные пути к ключевым директориям и файлам
CONFIG_DIR="${MTPX_ROOT}/config"            # Конфигурация (domains.txt, proxy-*.py)
STATE_DIR="${MTPX_ROOT}/state"              # Состояние (секреты, пользователи, runtime)
LIB_DIR="${MTPX_ROOT}/lib"                  # Библиотечные модули

DOMAINS_FILE="${CONFIG_DIR}/domains.txt"    # Список доменов (первый = текущий)
USERS_FILE="${STATE_DIR}/users.csv"         # Список пользователей
SECRETS_FILE="${STATE_DIR}/secrets.csv"     # CSV с секретами
RUNTIME_FILE="${STATE_DIR}/runtime.env"     # Runtime-параметры (порт, последнее применение)
AUTO_TICK_FILE="${STATE_DIR}/auto_tick.env" # Настройки авто-ротации (если понадобятся) доменов

# Имя Docker-контейнера по умолчанию
CONTAINER_NAME="mtproto-proxy"
DEFAULT_PORT="443"              # Порт по умолчанию (Fake TLS требует 443-подобный)
DEFAULT_DOMAIN="ya.ru"          # Домен для Fake TLS по умолчанию

# ─────────────────────────────────────────────────────────────────────────────
# ensure_dirs — создать все необходимые директории
# ─────────────────────────────────────────────────────────────────────────────
# Вызывается при `mtpx init`. mkdir -p безопасен: если директории уже есть —
# ошибки не будет. Это гарантирует, что все последующие операции записи
# не упадут из-за отсутствия пути.
# ─────────────────────────────────────────────────────────────────────────────
ensure_dirs() {
  mkdir -p "${CONFIG_DIR}" "${STATE_DIR}" "${LIB_DIR}"
}

# ─────────────────────────────────────────────────────────────────────────────
# atomic_write — атомарная запись в файл
# ─────────────────────────────────────────────────────────────────────────────
# Проблема: если записывать напрямую в файл и процесс прервётся (Ctrl+C,
# разрыв ssh), файл окажется повреждён / наполовину записан.
# Решение:
#   1. Создаём временный файл рядом с целевым (mktemp <file>.tmp.XXXXXX)
#   2. Пишем в него содержимое (из аргумента или stdin через cat)
#   3. Ставим chmod 600 (защита от чтения другими пользователями)
#   4. Атомарно переименовываем (mv -f) — на одной ФС это атомарная операция
#
# Использование:
#   atomic_write /path/to/file "содержимое"
#   atomic_write /path/to/file <<< "содержимое из stdin"
# ─────────────────────────────────────────────────────────────────────────────
atomic_write() {
  local target="$1"
  local tmp
  tmp="$(mktemp "${target}.tmp.XXXXXX")"
  if [[ $# -gt 1 ]]; then
    # Второй аргумент — строка для записи
    printf '%s\n' "$2" > "$tmp"
  else
    # Читаем из stdin (heredoc, pipe)
    cat > "$tmp"
  fi
  chmod 600 "$tmp"
  mv -f "$tmp" "$target"
}

# ─────────────────────────────────────────────────────────────────────────────
# chmod_sensitive — установить права 600 на чувствительные файлы
# ─────────────────────────────────────────────────────────────────────────────
# Секреты и runtime могут содержать конфиденциальные данные.
# Права 600 означают: владелец читает+пишет, остальные — ничего.
# || true — не падаем, если файла ещё нет (например, до init).
# ─────────────────────────────────────────────────────────────────────────────
chmod_sensitive() {
  chmod 600 "${SECRETS_FILE}" 2>/dev/null || true
  chmod 600 "${USERS_FILE}" 2>/dev/null || true
  chmod 600 "${RUNTIME_FILE}" 2>/dev/null || true
  chmod 600 "${AUTO_TICK_FILE}" 2>/dev/null || true
}

# ─────────────────────────────────────────────────────────────────────────────
# validate_port — проверить, что аргумент является корректным портом
# ─────────────────────────────────────────────────────────────────────────────
# Порт — число от 1 до 65535. Регулярка ^[0-9]+$ отсекает не-числовые строки,
# а арифметическая проверка — диапазоны (в т.ч. отрицательные и 0).
# ─────────────────────────────────────────────────────────────────────────────
validate_port() {
  local port="$1"
  if ! [[ "$port" =~ ^[0-9]+$ ]] || (( port < 1 || port > 65535 )); then
    log_error "Некорректный порт: $port"
    return 1
  fi
}

# ─────────────────────────────────────────────────────────────────────────────
# validate_domain — проверка валидности доменного имени
# ─────────────────────────────────────────────────────────────────────────────
# Правила RFC 1035 (упрощённо):
#   • Начинается с буквы или цифры
#   • Может содержать буквы, цифры, точки, дефисы, подчёркивания
#   • Заканчивается буквой или цифрой
#   • Минимальная длина — 1 символ (одиночная буква проходит)
# ─────────────────────────────────────────────────────────────────────────────
validate_domain() {
  local domain="$1"
  if ! [[ "$domain" =~ ^[a-zA-Z0-9]([a-zA-Z0-9._-]*[a-zA-Z0-9])?$ ]]; then
    log_error "Некорректный домен: $domain"
    return 1
  fi
}

# ─────────────────────────────────────────────────────────────────────────────
# validate_secret — проверка hex-секрета (минимум 30 символов)
# ─────────────────────────────────────────────────────────────────────────────
# Используется для базовой валидации перед добавлением в CSV.
# Секреты MTProto — это hex-строки, поэтому только [a-f0-9].
# ─────────────────────────────────────────────────────────────────────────────
validate_secret() {
  local secret="$1"
  if ! [[ "$secret" =~ ^[a-f0-9]{30,}$ ]]; then
    log_error "Некорректный секрет (ожидается hex 30+ символов): $secret"
    return 1
  fi
}

# ─────────────────────────────────────────────────────────────────────────────
# validate_hex_secret — строгая проверка MTProto-секрета
# ─────────────────────────────────────────────────────────────────────────────
# MTProto поддерживает три формата секретов:
#   • ee… — Fake TLS (TLS-обфускация, домен кодируется в hex)
#   • dd… — Простой (простой прокси без TLS)
#   • <hex> — Secure (рандомный, 32 байта)
# Здесь проверяем первые два формата с префиксом.
# ─────────────────────────────────────────────────────────────────────────────
validate_hex_secret() {
  local secret="$1"
  if ! [[ "$secret" =~ ^(ee|dd)[a-f0-9]{28,}$ ]]; then
    log_error "Некорректный MTProto секрет: $secret"
    return 1
  fi
}

# ─────────────────────────────────────────────────────────────────────────────
# generate_fake_tls_secret — сгенерировать Fake TLS секрет
# ─────────────────────────────────────────────────────────────────────────────
# Алгоритм:
#   1. Берём домен (по умолчанию ya.ru)
#   2. Конвертируем в hex через xxd (каждый байт → 2 hex-символа)
#   3. Дополняем случайными hex-символами до суммарной длины 30 символов
#   4. Префикс "ee" — сигнализирует клиенту MTProxy о Fake TLS режиме
#   5. Результат: ee + hex(домен) + random
#
# Зачем 30 символов? MTProto ожидает секрет длиной 16-32 байта (32-64 hex).
# 30 hex-символов = 15 байт — минимально допустимая длина для Fake TLS.
# ─────────────────────────────────────────────────────────────────────────────
generate_fake_tls_secret() {
  local domain="${1:-$DEFAULT_DOMAIN}"
  local domain_hex
  domain_hex=$(printf '%s' "$domain" | xxd -ps | tr -d '\n')
  local domain_len=${#domain_hex}
  local needed=$((30 - domain_len))
  if (( needed < 1 )); then
    needed=1
  fi
  local random_hex
  random_hex=$(openssl rand -hex 16 | cut -c1-"$needed")
  printf 'ee%s%s' "$domain_hex" "$random_hex"
}

# ─────────────────────────────────────────────────────────────────────────────
# generate_simple_secret — сгенерировать простой 16-байтный hex-секрет
# ─────────────────────────────────────────────────────────────────────────────
# Используется для режима без Fake TLS (простой MTProto прокси).
# openssl rand -hex 16 → 32 hex-символа (16 байт энтропии).
# ─────────────────────────────────────────────────────────────────────────────
generate_simple_secret() {
  openssl rand -hex 16
}

# ─────────────────────────────────────────────────────────────────────────────
# normalize_domain — домен → имя для файла/контейнера
# ya.ru → ya-ru, cloudflare.com → cloudflare-com
# ─────────────────────────────────────────────────────────────────────────────
normalize_domain() {
  printf '%s' "$1" | tr '.' '-' | tr '_' '-' | tr '[:upper:]' '[:lower:]'
}

# ─────────────────────────────────────────────────────────────────────────────
# container_name_for_domain — полное имя контейнера для домена
# ─────────────────────────────────────────────────────────────────────────────
container_name_for_domain() {
  local norm
  norm=$(normalize_domain "$1")
  printf 'mtproto-%s' "$norm"
}

# ─────────────────────────────────────────────────────────────────────────────
# require_cmd — проверить, что команда доступна в PATH
# ─────────────────────────────────────────────────────────────────────────────
# command -v возвращает 0, если команда найдена, и 1 — если нет.
# Перенаправление &>/dev/null подавляет вывод пути команды.
# ─────────────────────────────────────────────────────────────────────────────
require_cmd() {
  local cmd="$1"
  if ! command -v "$cmd" &>/dev/null; then
    log_error "Требуется '$cmd', но он не найден в PATH"
    return 1
  fi
}

# ─────────────────────────────────────────────────────────────────────────────
# require_all_cmds — проверить набор команд за один вызов
# ─────────────────────────────────────────────────────────────────────────────
# Вызывает require_cmd для каждого аргумента и суммирует ошибки.
# Возвращает 1, если хотя бы одна команда отсутствует.
# ─────────────────────────────────────────────────────────────────────────────
require_all_cmds() {
  local missing=0
  for cmd in "$@"; do
    require_cmd "$cmd" || missing=1
  done
  return $missing
}

# ─────────────────────────────────────────────────────────────────────────────
# load_env — загрузить .env-файл в текущее окружение
# ─────────────────────────────────────────────────────────────────────────────
# set -a / set +a — автоматический экспорт всех переменных, заданных через
# source. Без этого переменные были бы локальными для текущего shell.
# Используется редко; предпочтительнее env_get / env_set.
# ─────────────────────────────────────────────────────────────────────────────
load_env() {
  local file="$1"
  if [[ -f "$file" ]]; then
    set -a
    # shellcheck source=/dev/null
    source "$file"
    set +a
  fi
}

# ─────────────────────────────────────────────────────────────────────────────
# env_get — прочитать значение ключа из .env-файла
# ─────────────────────────────────────────────────────────────────────────────
# Алгоритм:
#   1. grep -E "^${key}=" — находим строку, начинающуюся с "KEY="
#   2. head -1 — берём первое вхождение (защита от дубликатов)
#   3. cut -d'=' -f2- — отрезаем ключ, оставляем всё после первого "="
#   4. tr -d "'" | tr -d '"' — убираем кавычки, если они были
# ─────────────────────────────────────────────────────────────────────────────
env_get() {
  local file="$1" key="$2"
  if [[ -f "$file" ]]; then
    grep -E "^${key}=" "$file" 2>/dev/null | head -1 | cut -d'=' -f2- | tr -d "'" | tr -d '"'
  fi
}

# ─────────────────────────────────────────────────────────────────────────────
# env_set — записать или обновить ключ в .env-файле
# ─────────────────────────────────────────────────────────────────────────────
# Атомарная операция (через temp + mv):
#   • Если ключ уже есть — заменяем строку через sed
#   • Если ключа нет — дописываем в конец
#   • Если файла нет — создаём с единственной строкой
# chmod 600 — защита чувствительных данных.
# ─────────────────────────────────────────────────────────────────────────────
env_set() {
  local file="$1" key="$2" value="$3"
  local tmp
  tmp="$(mktemp "${file}.tmp.XXXXXX")"
  if [[ -f "$file" ]]; then
    if grep -qE "^${key}=" "$file" 2>/dev/null; then
      # Ключ существует — заменяем строку
      sed "s|^${key}=.*|${key}=${value}|" "$file" > "$tmp"
    else
      # Ключа нет — копируем файл и дописываем
      cat "$file" > "$tmp"
      printf '%s=%s\n' "$key" "$value" >> "$tmp"
    fi
  else
    # Файла нет — создаём
    printf '%s=%s\n' "$key" "$value" > "$tmp"
  fi
  chmod 600 "$tmp"
  mv -f "$tmp" "$file"
}

# ─────────────────────────────────────────────────────────────────────────────
# ensure_sudo — убедиться, что скрипт работает с root-правами
# ─────────────────────────────────────────────────────────────────────────────
# Docker обычно требует root (или пользователь в группе docker).
# Проверяем UID: если не 0 — пытаемся sudo -v (проверка без выполнения).
# Если sudo нет — выводим ошибку.
# ─────────────────────────────────────────────────────────────────────────────
ensure_sudo() {
  if [[ "$(id -u)" -ne 0 ]]; then
    if command -v sudo &>/dev/null; then
      sudo -v || { log_error "Требуется sudo, но авторизация не удалась"; return 1; }
    else
      log_error "Запустите от root или установите sudo"
      return 1
    fi
  fi
}

# ─────────────────────────────────────────────────────────────────────────────
# mask_secret — замаскировать секрет для безопасного вывода
# ─────────────────────────────────────────────────────────────────────────────
# Показываем первые 6 и последние 4 символа, остальное → звёздочки.
# Если секрет короче 8 символов — выводим целиком (слишком короткий для
# маскировки). Это предотвращает случайную утечку полного секрета в логи
# или на экран при демонстрации.
# ─────────────────────────────────────────────────────────────────────────────
mask_secret() {
  local secret="$1"
  local len=${#secret}
  if (( len <= 8 )); then
    printf '%s' "$secret"
  else
    # Первые 6 символов
    printf '%s' "${secret:0:6}"
    # Звёздочки вместо середины (len - 10 = общая длина минус 6 и 4)
    printf '%*s' $(( len - 10 )) '' | tr ' ' '*'
    # Последние 4 символа
    printf '%s' "${secret: -4}"
  fi
}

# ─────────────────────────────────────────────────────────────────────────────
# get_server_ip — определить публичный IP сервера
# ─────────────────────────────────────────────────────────────────────────────
# Пробуем два внешних сервиса с таймаутом 5 секунд каждый.
# -4 — форсируем IPv4 (без этого curl может отдать IPv6-адрес сервера).
# Если оба недоступны — возвращаем "unknown" (не падаем).
# ─────────────────────────────────────────────────────────────────────────────
get_server_ip() {
  curl -s -4 --max-time 5 ifconfig.me 2>/dev/null || \
  curl -s -4 --max-time 5 api.ipify.org 2>/dev/null || \
  echo "unknown"
}

# ─────────────────────────────────────────────────────────────────────────────
# port_in_use — проверить, занят ли порт
# ─────────────────────────────────────────────────────────────────────────────
# Предпочитаем ss (современная замена netstat), fallback на netstat.
# Ищем точное совпадение ":<port> " (пробел после порта важен — иначе
# порт 443 матчится на 8443, 4430 и т.д.).
# Возвращает 0 если порт занят, 1 — если свободен.
# ─────────────────────────────────────────────────────────────────────────────
port_in_use() {
  local port="$1"
  if command -v ss &>/dev/null; then
    ss -tuln 2>/dev/null | grep -qE ":${port} "
  elif command -v netstat &>/dev/null; then
    netstat -tuln 2>/dev/null | grep -qE ":${port} "
  else
    # Если ни ss, ни netstat нет — считаем, что порт свободен
    # (лучше попытаться забиндить, чем пропустить)
    return 1
  fi
}

# ─────────────────────────────────────────────────────────────────────────────
# find_free_port — найти первый свободный порт из списка
# ─────────────────────────────────────────────────────────────────────────────
# Первый аргумент — предпочтительный порт, остальные — альтернативы.
# Возвращает первый свободный порт через echo, код 0.
# Если все заняты — код 1.
# ─────────────────────────────────────────────────────────────────────────────
find_free_port() {
  local preferred="$1"; shift
  if ! port_in_use "$preferred"; then
    echo "$preferred"
    return 0
  fi
  for alt in "$@"; do
    if ! port_in_use "$alt"; then
      echo "$alt"
      return 0
    fi
  done
  return 1
}
